    private void jTable6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable6MouseClicked
        int row=jTable6.getSelectedRow();
        try {

            ResultSet resultset=  MySQL.execute("SELECT * FROM `grn_item` INNER JOIN `stock` ON `stock`.`id`=`grn_item`.`stock_id` "
                    + "INNER JOIN `product` ON `stock`.`product_id` =`product`.`id`"
                    + " INNER JOIN `brand` ON `brand`.`id` =`product`.`brand_id` "
                    + "INNER JOIN `category` ON `product`.`category_id`=`category`.`id`  WHERE `grn_id` ='"+String.valueOf(jTable6.getValueAt(row, 0))+"'");

            jLabel32.setText(String.valueOf(jTable6.getValueAt(row, 0)));
            jLabel34.setText(String.valueOf(jTable6.getValueAt(row, 3)));
            DefaultTableModel model=(DefaultTableModel) jTable7.getModel();
            model.setRowCount(0);
            while (resultset.next()) {

                Vector vector=new Vector();
                vector.add(resultset.getString("stock_id"));
                vector.add(resultset.getString("brand.brand"));
                vector.add(resultset.getString("product.name"));
                vector.add(resultset.getString("category.category"));
                vector.add(resultset.getString("buying_price"));
                vector.add(resultset.getString("qty"));

                model.addRow(vector);
                jTable7.setModel(model);
            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "An error occurred while trying to log in", e);
        }
    }//GEN-LAST:event_jTable6MouseClicked


