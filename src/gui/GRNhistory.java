        private void loadTable() {

        try {
            String query = "SELECT * FROM `grn` INNER JOIN `supplier` ON `supplier`.`mobile` =`grn`.`supplier_mobile` INNER JOIN `user` ON `user`.`id`=`grn`.`user_id`";
            //search mobile
            String mobile = jTextField11.getText();
            if (!mobile.isEmpty()) {
                query += "WHERE `supplier_mobile` LIKE '" + mobile + "%'";
            }

            //employee
            String user_mobile ;

            if (!jTextField12.getText().isEmpty()) {
                if (query.contains("WHERE")) {
                    query += " AND ";
                } else {
                    query += "WHERE ";
                }
                user_mobile=jTextField12.getText();
                query+="`user`.`mobile` LIKE '" + user_mobile + "%'";
            }

            //Date
            Date date = null;

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

            if (jDateChooser6.getDate() != null) {
                date = jDateChooser6.getDate();
                query += "AND `grn`.`date` LIKE '" + sdf.format(date) + "%'";
            }

            System.out.println(query);
            ResultSet resultset = MySQL.execute(query);

            DefaultTableModel model = (DefaultTableModel) jTable6.getModel();
            model.setRowCount(0);
            while (resultset.next()) {
                String name = resultset.getString("supplier.fname") + " " + resultset.getString("supplier.lname");
                Vector vector = new Vector();
                vector.add(resultset.getString("grn.id"));
                vector.add(name);
                vector.add(resultset.getString("supplier_mobile"));
                vector.add(resultset.getString("date"));
                vector.add(resultset.getString("paid_amount"));
                vector.add(resultset.getString("user.mobile"));

                model.addRow(vector);
                jTable6.setModel(model);
            }

        } catch (Exception e) {
            logger.log(Level.WARNING, "An error occurred while trying to log in", e);
        }
    }


