    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
      if(evt.getClickCount() == 1){
        jTextField2.setText(String.valueOf(jTable1.getValueAt(row, 0)));
        jTextField3.setText(String.valueOf(jTable1.getValueAt(row, 1)));
        jTextField4.setText(String.valueOf(jTable1.getValueAt(row, 2)));
        jTextField5.setText(String.valueOf(jTable1.getValueAt(row, 3)));
        jTextField1.setText(String.valueOf(jTable1.getValueAt(row, 4)));
        jTextField2.setEditable(false);
      }else if(evt.getClickCount()==2){
      
          String name=String.valueOf(jTable1.getValueAt(row, 1))+" "+String.valueOf(jTable1.getValueAt(row, 2));
          String mobile=String.valueOf(jTable1.getValueAt(row, 0));
          grn.getjLabel11().setText(name);
          grn.getjLabel13().setText(mobile);
          this.dispose();
          
      }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `supplier` WHERE `mobile` ='" + jTextField2.getText() + "'");
            if (rs.next()) {

                JOptionPane.showMessageDialog(this, "Supplier Already Added", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                if (jTextField1.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Select Company", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField2.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Supplier Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField3.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Supplier First Name", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField4.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Supplier Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
                } else if (jTextField5.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please Enter Supplier Email", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    if (jTextField1.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Please Enter Supplier Name", "Warning", JOptionPane.WARNING_MESSAGE);
                    } else {

                        MySQL.execute("INSERT INTO `supplier` (`mobile`,`fname`,`lname`,`email`,`company_id`) VALUES ('" + jTextField2.getText() + "','" + jTextField3.getText() + "','" + jTextField4.getText() + "','" + jTextField5.getText() + "','" + ComapanyId + "')");
                        JOptionPane.showMessageDialog(this, "Supplier Added", "Succesfull", JOptionPane.PLAIN_MESSAGE);
                        reset();

                    }

                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

