    private void loadSupplier() {
        try {

            String query="SELECT * FROM `supplier` INNER JOIN `company` ON `supplier`.`company_id` =`company`.`id` ";

            if(jComboBox1.getSelectedIndex()==0){
            query+="ORDER BY `fname` ASC";
            }else if(jComboBox1.getSelectedIndex()==1){
            query+="ORDER BY `fname` DESC";
            }else if(jComboBox1.getSelectedIndex()==2){
            query+="ORDER BY `name` ASC";
            }else if(jComboBox1.getSelectedIndex()==3){
            query+="ORDER BY `name` DESC";
            }
            System.out.println(query);

            ResultSet rs = MySQL.execute(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString("mobile"));
                vector.add(rs.getString("fname"));
                vector.add(rs.getString("lname"));
                vector.add(rs.getString("email"));
                vector.add(rs.getString("name"));
                model.addRow(vector);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

