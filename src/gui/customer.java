    private HashMap<String, String> statusmap = new HashMap();

    private void loadstatus() {
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `status`");
            Vector v = new Vector();
            v.add("select");
            while (resultset.next()) {
                v.add(resultset.getString("status"));
                statusmap.put(resultset.getString("status"), resultset.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            logger.log(Level.WARNING, "An error occurred while trying to log in", e);
        }

    }

    private void loadusers() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            String query="SELECT * FROM `customer` INNER JOIN `status` ON `customer`.`status_id` =`status`.`id`";
            
            if(!userSearchField.getText().isEmpty()){
            query+=" WHERE `mobile` LIKE '"+userSearchField.getText()+"%'";
            }
            
            ResultSet resultset = MySQL.execute(query);
            while (resultset.next()) {

                Vector v = new Vector();
                v.add(resultset.getString("mobile"));
                v.add(resultset.getString("fname"));
                v.add(resultset.getString("lname"));
                v.add(resultset.getString("points"));
                v.add(resultset.getString("status.status"));

                model.addRow(v);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            logger.log(Level.WARNING, "An error occurred while trying to log in", e);
        }

    }

    boolean valid = false;

    private void validation() {

        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String mobile = jTextField3.getText();
        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter First Name", "Alert", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Last Name", "Alert", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Mobile", "Alert", JOptionPane.WARNING_MESSAGE);
        } else if (jComboBox2.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please Select Status", "Alert", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "invalid  mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            valid = true;
        }

    }

